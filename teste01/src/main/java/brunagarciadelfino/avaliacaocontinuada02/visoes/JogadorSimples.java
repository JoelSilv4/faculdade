package brunagarciadelfino.avaliacaocontinuada02.visoes;

public class JogadorSimples {

    private Integer id;
    private String nome;

    public JogadorSimples(Integer id, String nome) {
        this.id = id;
        this.nome = nome;
    }

    public Integer getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }
}
